#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*cpy;
	char	*src;
	size_t	i;

	src = (char *)s;
	i = 0;
	cpy = malloc(sizeof(*cpy) * ft_strlen(src));
	while (src[i])
	{
		cpy[i] = src[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
