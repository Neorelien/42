#include "libft.h"

void	ft_putnbr_fd(int n1, int fd)
{
	char			c;
	unsigned int	n;

	if (n1 < 0)
	{
		n = n1 * -1;
		write(fd, "-", 1);
	}
	else
		n = n1;
	if (n > 9)
	{
		c = (n % 10) + 48;
		ft_putnbr_fd(n / 10, fd);
		write(fd, &c, 1);
	}
	else
	{
		c = n + 48;
		write(fd, &c, 1);
	}
}
