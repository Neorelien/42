#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*temp;
	size_t	i;

	i = 0;
	temp = s;
	while (n-- > 0)
	{
		temp[i] = c;
		i++;
	}
	return (s);
}
