#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*temp;
	int		i;

	if (n == 0)
		return ;
	temp = s;
	i = 0;
	while (n--)
	{
		temp[i] = 0;
		i++;
	}
}
