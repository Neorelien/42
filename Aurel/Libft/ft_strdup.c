#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*cpy;
	int		i;
	char	*src;

	src = (char*)s1;
	i = 0;
	if (!(cpy = malloc(sizeof(*cpy) * ft_strlen(src))))
		return (0);
	while (src[i])
	{
		cpy[i] = src[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
